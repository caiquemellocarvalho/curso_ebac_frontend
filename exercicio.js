"use strict";
function multiplicar(num1, num2) {
    return num1 * num2;
}
function saudacao(nome) {
    return `Olá ${nome}`;
}
const resultadoMultiplicacao = multiplicar(5, 10); // Retorna 50
const mensagemSaudacao = saudacao("Maria"); // Retorna "Olá Maria"
console.log(resultadoMultiplicacao);
console.log(mensagemSaudacao);
